#!/bin/bash

# Проверяем, что переданы два аргумента
if [ "$#" -ne 2 ]; then
    echo "Использование: $0 <имя_файла> <слово_для_поиска>"
    exit 1
fi

FILE=$1
WORD=$2

# Проверяем, существует ли файл
if [ ! -f "$FILE" ]; then
    echo "Ошибка: Файл '$FILE' не найден."
    exit 1
fi

# Считаем количество вхождений слова в файле
COUNT=$(grep -o -i "$WORD" "$FILE" | wc -l)

echo "Слово '$WORD' встречается в файле '$FILE' $COUNT раз."
